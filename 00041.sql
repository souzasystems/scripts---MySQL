CREATE TABLE IF NOT EXISTS LANCAMENTOS (
    ID_LANCAMENTO INT AUTO_INCREMENT,
    ID_FORMA_PAGAMENTO TINYINT NOT NULL,
    ID_PESSOA INT NOT NULL,
    TIPO_LANCAMENTO VARCHAR(05) NULL,
    DATA_EMISSAO DATE NULL,   
    NUMERO_NOTA_FISCAL VARCHAR(20) NULL,
    DATA_PAGAMENTO DATE NULL,
    DATA_VENCIMENTO DATE NULL,
    DATA_PREVISAO_PAGAMENTO DATE NULL,
    VALOR_TOTAL DECIMAL(15, 2) NULL,
    OBSERVACOES VARCHAR(255) NULL,
    LOG_ID_USUARIO SMALLINT NOT NULL,
    LOG_ROTINA VARCHAR(1) NOT NULL,
    LOG_DATA_HORA DATETIME DEFAULT CURRENT_TIMESTAMP,
    LOG_MOTIVO_EXCLUSAO VARCHAR(255),
    CON_CURRENCY_ID VARCHAR(50) NOT NULL DEFAULT (UUID()),
   CONSTRAINT PK_LANCAMENTOS PRIMARY KEY (ID_LANCAMENTO),
   CONSTRAINT FK_LANCAMENTOS_FORMAS_PAGTOS FOREIGN KEY(ID_FORMA_PAGAMENTO) REFERENCES FORMAS_PAGAMENTO(ID_FORMA_PAGAMENTO),
   CONSTRAINT FK_LANCAMENTOS_PESSOAS FOREIGN KEY(ID_PESSOA) REFERENCES PESSOAS(ID_PESSOA),
   CONSTRAINT FK_LANCAMENTOS_USUARIOS FOREIGN KEY(LOG_ID_USUARIO) REFERENCES USUARIOS(ID_USUARIO)
);